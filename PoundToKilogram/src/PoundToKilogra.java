public class PoundToKilogra {
    public static void main(String[] args) {

        //Converted a give pound number into a kilogram
        // 1 pound = 0.45359237d
        double numPound = 200;
        double convertedKilogram = numPound *  0.45359237d;
        System.out.println("Kilograms " + convertedKilogram);
    }
}
